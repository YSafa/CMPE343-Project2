    // ========== DATABASE AUTH ==========
    /**
     * Authenticates the user against the database using case-sensitive credentials.
     *
     * @param username the entered username
     * @param password the entered password
     * @return a {@link User} object representing the logged-in user, or null if invalid
     */
    public static User authenticate(String username, String password)
    {
        try (Connection conn = DriverManager.getConnection(DB_URL, USER, PASS);
             PreparedStatement stmt = conn.prepareStatement(
                     "SELECT * FROM users WHERE BINARY username=? AND BINARY password_hash=?")) {
            stmt.setString(1, username);
            stmt.setString(2, password);
            ResultSet rs = stmt.executeQuery();
            if (rs.next())
            {
                String role = rs.getString("role").trim();
                User user = null;

                switch (role) {
                    case "TESTER":
                        user = new Tester(rs);
                        break;
                    case "JUNIOR_DEV":
                        user = new JuniorDeveloper(rs);
                        break;
                    case "SENIOR_DEV":
                        user = new SeniorDeveloper(rs);
                        break;
                    case "MANAGER":
                        user = new Manager(rs);
                        break;
                    default:
                        user = null;
                        break;
                }

                return user;

            }
        } catch (SQLException e) {
            System.out.println(RED + "Database error: " + e.getMessage() + RESET);
        }
        return null;
    }

    // ========== ABSTRACT USER ==========
    /**
     * Represents a generic system user.
     * Extended by specific user role classes (Tester, Developer, Manager).
     */
    static abstract class User
    {
        protected int id;
        protected String username, firstName, lastName, role;

        public User(ResultSet rs) throws SQLException
        {
            this.id = rs.getInt("user_id");
            this.username = rs.getString("username");
            this.firstName = rs.getString("first_name");
            this.lastName = rs.getString("last_name");
            this.role = rs.getString("role");
        }

        /**
         * Displays the role-specific menu options.
         */
        abstract void displayMenu();

        /**
         * Changes the user's password in the database.
         *
         * @param newPass the new password
         */
        void changePassword(String newPass)
        {
            try (Connection conn = DriverManager.getConnection(DB_URL, USER, PASS);
                 PreparedStatement stmt = conn.prepareStatement(
                         "UPDATE users SET password_hash=? WHERE user_id=?")) {
                stmt.setString(1, hashPassword(newPass));
                stmt.setInt(2, id);
                stmt.executeUpdate();
                System.out.println(GREEN + "Password updated successfully." + RESET);
            } catch (SQLException e) {
                System.out.println(RED + "Error updating password: " + e.getMessage() + RESET);
            }
        }
    }

